package com.zhjy.zhjy.beans;

public class Treads {
    private String heardpic_url;
    private String phone;
    private String nickname;
    private String treads_picture_url;
    private String content;
    private int age;
    private String location;
    private String hometown;
    private String date_time;

    public String getHeardpic_url() {
        return heardpic_url;
    }

    public void setHeardpic_url(String heardpic_url) {
        this.heardpic_url = heardpic_url;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getNickname() {
        return nickname;
    }

    public void setNickname(String nickname) {
        this.nickname = nickname;
    }

    public String getTreads_picture_url() {
        return treads_picture_url;
    }

    public void setTreads_picture_url(String treads_picture_url) {
        this.treads_picture_url = treads_picture_url;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getHometown() {
        return hometown;
    }

    public void setHometown(String hometown) {
        this.hometown = hometown;
    }

    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }
}
